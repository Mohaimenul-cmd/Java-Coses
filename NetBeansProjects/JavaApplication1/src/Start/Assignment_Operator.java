public class Assignment_Operator {
   
    public static void main(String[] args) {
        
        /*
        
        +=
        -=
        %=
        *=
        
        */
        
       int x = 2;
       int y = 4;
       
       
       x+=y; // x=x+y 
       y-=x;  // y=y-x
       
       
       System.out.println("x is:"+x);
       System.out.println("y is :"+y);
       
     
}
}